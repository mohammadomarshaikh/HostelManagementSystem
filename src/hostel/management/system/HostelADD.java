    public void validateFields() {
        String hostelidtxt = txtHostelID.getText();
        String hostelnametxt = txtHostelName.getText();
        String noofroomstxt = txtnoofrooms.getText();
        String noofstudents = txtnoofstudents.getText();

        if (!hostelidtxt.isEmpty() && !hostelnametxt.isEmpty() && !noofroomstxt.isEmpty() && !noofstudents.isEmpty()) {
            btnSave.setEnabled(true);
        } else {
            btnSave.setEnabled(false);
        }
    }

