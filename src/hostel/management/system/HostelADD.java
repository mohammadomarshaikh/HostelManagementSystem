    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Fields are valid, proceed with saving
        String hostelID = txtHostelID.getText();
        String hostelName = txtHostelName.getText();
        String noOfRooms = txtnoofrooms.getText();
        String noOfStudents = txtnoofstudents.getText();

        // Create a Hostel object
        Hostel hostel = new Hostel();
        hostel.setHostel_ID(hostelID);
        hostel.setHostel_name(hostelName);
        hostel.setNo_of_rooms(noOfRooms);
        hostel.setNo_of_students(noOfStudents);

        // Save the Hostel object to the database
        HostelDao.save(hostel);

        // Clear the text fields if needed
        txtHostelID.setText("");
        txtHostelName.setText("");
        txtnoofrooms.setText("");
        txtnoofstudents.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

