    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Fields are valid, proceed with saving
        String studentID = txtstudentid.getText();
        String firstName = txtfname.getText();
        String lastName = txtlastname.getText();
        String mobileNumber = txtmobilenumber.getText();
        String department = txtdepartment.getText();
        String year = txtyear.getText();
        String hostelID = txthostelid.getText();
        String roomID = txtroomid.getText();

        // Create a Student object
        Student student = new Student();
        student.setStudent_ID(studentID);
        student.setFname(firstName);
        student.setLname(lastName);
        student.setMob_no(mobileNumber);
        student.setDept(department);
        student.setYear(year);
        student.setHostel_ID(hostelID);
        student.setRoom_ID(roomID);

        // Save the Student object to the database
        StudentDao.save(student);

    // Clear the text fields
    txtstudentid.setText("");
    txtfname.setText("");
    txtlastname.setText("");
    txtmobilenumber.setText("");
    txtdepartment.setText("");
    txtyear.setText("");
    txthostelid.setText("");
    txtroomid.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

