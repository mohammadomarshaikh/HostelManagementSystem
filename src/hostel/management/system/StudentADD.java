public void validateFields() {
    String studentIDTxt = txtstudentid.getText();
    String firstNameTxt = txtfname.getText();
    String lastNameTxt = txtlastname.getText();
    String mobileNumberTxt = txtmobilenumber.getText();
    String departmentTxt = txtdepartment.getText();
    String yearTxt = txtyear.getText();
    String hostelIDTxt = txthostelid.getText();
    String roomIDTxt = txtroomid.getText();

    if (!studentIDTxt.isEmpty() && !firstNameTxt.isEmpty() && !lastNameTxt.isEmpty() &&
        !mobileNumberTxt.isEmpty() && !departmentTxt.isEmpty() && !yearTxt.isEmpty() &&
        !hostelIDTxt.isEmpty() && !roomIDTxt.isEmpty()) {
        btnSave.setEnabled(true);
    } else {
        btnSave.setEnabled(false);
    }
}


