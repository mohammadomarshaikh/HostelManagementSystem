    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<Visitor> list = VisitorDao.getAllRecords();
        Iterator<Visitor> itr = list.iterator();
        while (itr.hasNext()) {
            Visitor visitorObj = itr.next();
            // Add a new row with the visitor's details
            dtm.addRow(new Object[]{
                visitorObj.getVisitor_ID(),
                visitorObj.getIn_time(),
                visitorObj.getOut_time(),
                visitorObj.getDate(),
                visitorObj.getVisitor_name(),
                visitorObj.getStudent_ID()
            });
        }
    }//GEN-LAST:event_formComponentShown

