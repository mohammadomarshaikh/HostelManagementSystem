    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        // Get the values from the text fields
        String visitorID = txtvisitorid.getText();
        String inTime = txtintime.getText();
        String outTime = txtouttime.getText();
        String date = txtdate.getText();
        String visitorName = txtvisitorname.getText();
        String studentID = txtstudentid.getText();

        // Create a Visitor object with the updated information
        Visitor updatedVisitor = new Visitor();
        updatedVisitor.setVisitor_ID(visitorID);
        updatedVisitor.setIn_time(inTime);
        updatedVisitor.setOut_time(outTime);
        updatedVisitor.setDate(date);
        updatedVisitor.setVisitor_name(visitorName);
        updatedVisitor.setStudent_ID(studentID);

        // Call the update method from your VisitorDao
        VisitorDao.update(updatedVisitor);

        // Optionally, you can perform any additional actions here
        // For example, show a success message, clear fields, or navigate to another page
        setVisible(false);
        new ViewEditDeleteVisitor().setVisible(true);        
    }//GEN-LAST:event_btnUpdateActionPerformed

