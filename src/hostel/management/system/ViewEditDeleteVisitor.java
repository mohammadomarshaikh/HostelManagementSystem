    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();

        // Get the values from the selected row in the table
        String visitorID = model.getValueAt(index, 0).toString();
        String inTime = model.getValueAt(index, 1).toString();
        String outTime = model.getValueAt(index, 2).toString();
        String date = model.getValueAt(index, 3).toString();
        String visitorName = model.getValueAt(index, 4).toString();
        String studentID = model.getValueAt(index, 5).toString();

        // Populate the text fields with the selected visitor's details
        txtvisitorid.setText(visitorID);
        txtintime.setText(inTime);
        txtouttime.setText(outTime);
        txtdate.setText(date);
        txtvisitorname.setText(visitorName);
        txtstudentid.setText(studentID);

        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);

    }//GEN-LAST:event_jTable1MouseClicked

