    public void validateFields() {
        String visitorIDTxt = txtvisitorid.getText();
        String inTimeTxt = txtintime.getText();
        String outTimeTxt = txtouttime.getText();
        String dateTxt = txtdate.getText();
        String visitorNameTxt = txtvisitorname.getText();
        String studentIDTxt = txtstudentid.getText();

        if (!visitorIDTxt.isEmpty() && !inTimeTxt.isEmpty() && !outTimeTxt.isEmpty()
                && !dateTxt.isEmpty() && !visitorNameTxt.isEmpty() && !studentIDTxt.isEmpty()) {
            btnUpdate.setEnabled(true);
        } else {
            btnUpdate.setEnabled(false);
        }
    }

